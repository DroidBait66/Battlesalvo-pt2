package cs3500.pa03.model;

/**
 * Represents one of the three possible results of a BattleSalvo game
 */
public enum GameResult {
  /**
   * Represents winning a BattleSalvo game
   */
  WIN,
  /**
   * Represents losing a BattleSalvo game
   */
  LOSE,
  /**
   * Represents tying a BattleSalvo game
   */
  TIE
}
